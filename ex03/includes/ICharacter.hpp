#ifndef ICHARACTER_HPP
# define ICHARACTER_HPP

# include <iostream>

// Forward declaration to avoid circular dependency
class AMateria;

/**
 * @brief	Interface for a character in the game.
 * 
 * This interface defines the methods that any character must implement,
 * including equipping, unequipping, and using materias.
 */
class ICharacter
{
	public:
		virtual ~ICharacter() {}
		virtual std::string const & getName() const = 0;
		virtual void equip(AMateria* m) = 0;
		virtual void unequip(int idx) = 0;
		virtual void use(int idx, ICharacter& target) = 0;
};

#endif
